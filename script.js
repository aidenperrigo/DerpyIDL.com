function shuffleHistoricalPositions(list, positions) {
    const shuffled = [...list];
    for (let i = 0; i < positions.length - 1; i++) {
        [shuffled[positions[i]], shuffled[positions[i + 1]]] = [shuffled[positions[i + 1]], shuffled[positions[i]]];
    }
    return shuffled;
}

